FROM PAPER P
INNER JOIN AUTHOR_PAPER AP ON P.ID = AP.PAPER_ID
INNER JOIN AUTHOR A ON A.ID = AP.AUTHOR_ID
WHERE --A.NAME LIKE '%Henning Fernau%'
A.ID IN (4159371,6581334,7394843,7420402,7648311,8201782,9356057,9276753,9334938)
GROUP BY A.ID;

